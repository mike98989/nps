            <h3><strong>THE ADMIN STRUCTURE</strong></h3>
              <p>
                At  the apex of the NPS sits the Controller-General of the Nigerian Prisons  Service.  He is the Chief Executive  Officer and is responsible for the formulations and implementation of penal  policies in Nigeria.  He is responsible  to the President through the Minister of Interior and the Civil Defence, Fire, Immigration  and Prisons Services Board which the Minister heads.  But in matters of prison policy he takes  direct responsibility for policy implementation.<br>
                He  is assisted by six (6) Deputy Controllers-General (DCGs) who head the six broad  administrative divisions called Directorates into which the Service is broken  for efficient management.  <br>
                The  Deputy Controllers-General who head the Directorates report to the  Controller-General and constitute together with the Controller-General the  highest decision-making body in the Administration of the Nigerian Prisons  Service.<br>
                The  Directorates are specialized divisions charged with the responsibility of  coordinating specific areas of prison administration. They are:</p>
