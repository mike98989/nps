                <li>Operations  </li>
                <li>Administration and Supplies  </li>
                <li>Health and Social Welfare </li>
                <li>Finance and Budget  </li>
                <li>Inmates&rsquo; Training and Productivity </li>
                <li>Works and Logistics  </li>
              </ol>
              
