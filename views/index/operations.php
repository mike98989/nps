              <h3><strong>Operations Directorate</strong></h3>
              
                <strong>The Operations Directorate has the  responsibility of, among others:</strong>
              <ol>
                <li>Ensuring that all prisons nation-wide are  administered according to law and managed in conformity with the United Nations  Standard Minimum Rules for the Treatment of Prisoners (The Mandela Rules);</li>
                <li>Ensuring the maintenance of acceptable standard of  activities in all prisons according to their security categorization;</li>
                <li>Ensuring proper control by setting appropriate  machinery in motion for effective monitoring of inmates for enhanced security  management: thwarting escapes, prevention and containment of riots and  generally providing secure environment for the staff and inmates in the  performance of prison duties;</li>
                <li>Collecting statistical data for planning and  research for improved service delivery;</li>
                <li>Enlightening the general public on the activities of  the Service thereby enlisting their cooperation toward the reintegration of  discharged inmates into the society;</li>
                <li>Securing escort duty staff as well as the inmates  they convey to and from courts and prisons;</li>
              </ol>
