             <h4><strong>INTRODUCTION</strong><br>
              </h4>
             <p>From its inception to date, the Nigerian Prisons Service has had 20 heads. <br>
                The first seven Directors of Prisons (as the office was then termed) had British nationality.<br>
                With effect from 1st October, 1992 the office of the head of the Nigerian Prisons Service was changed from Director of Prisons to Controller-General of Prisons and the then Director of Prisons, J. O. Lily Ojo automatically became the first Controller-General of Prisons.<br>
                Below is a comprehensive list of all who had headed the Prisons in Nigeria, their years of entry and exit, and profile </p>
