            <h3><strong>Administration and Supplies  Directorate</strong></h3>
              
                The Administration, and Supplies directorate  is charged with the responsibility of properly articulating and implementing  policy on personnel matters such as appointment, promotion, staff distribution,  posting, discipline and training.  This  is the Directorate that sees to the recruitment and documentation of new  staff.  It also coordinates promotions  and plans the training programmes of the various training institutions which it  supervises and ensures the discipline of staff of all categories.  In addition to the above, the Directorate also  sees to the proper processing of staff wastages through:
              <ol>
                <li>Death</li>
                <li>Voluntary retirements</li>
                <li>Compulsory retirements</li>
                <li>Dismissals / Terminations etc.</li>
              </ol>
              <p>Through its  pension unit, it ensures that retired/dead officers&rsquo; entitlements are promptly  and properly computed and paid.<br>
                The  supplies section is charged with the responsibility of:</p>
              <ol>
                <li>Administering the prison stores.</li>
                <li>Procuring and distributing the necessary stores and  generally seeing to the regular provision of the stores necessary for the  smooth running of the Service.</li>
              </ol>
