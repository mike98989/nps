            <h3><strong>THE COMMAND STRUCTURE</strong></h3>
              <p>The Nigerian Prisons Service operates a four level Command</p>
              <p>Structure with the National Headquarters in Abuja.</p>
              <p>The Structure is divided into eight (8) zones, each zone having State</p>
              <p>Commands under it.</p>
              <p>The Zones are headed by Assistant Controller-Generals of Prisons.</p>
              <p>The States are headed by Controllers of Prisons.</p>
              <p>The prison is the last unit of the structure.</p>
              
