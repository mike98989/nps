               <h3><strong>FUNCTION OF NPS </strong></h3>
              <p>
              The Nigerian Prisons Service derives its operational powers from CAP P29 Laws of the Federation of Nigeria 2004 to<br>
                i.	take into lawful custody all those certified to be so kept by courts of competent jurisdiction;<br>
                ii.	produce suspects in courts as and when due;<br>
                iii.	identify the causes of their anti-social dispositions;<br>
                iv.	set in motion mechanisms for their treatment and training for eventual reintegration into society as normal law abiding citizens on discharge; and <br>
                v.	Administer Prisons Farms and Industries for this purpose and in the process generate revenue for the government.<br>
